/**
 * Domain: reading model for the atlas navigation surface (captain-atlas 15).
 *
 * Single responsibility: turn the parsed nav-atlas artifact set into READING
 * objects — named system journeys, route diagrams, route theses, stage roles,
 * anchor status, coverage regions, top probes, and agent-handoff queries — that
 * the shell renders as a walkthrough-first reading experience.
 *
 * Why this is fixture/profile-authored, not a generic template: the
 * charter-08 + doc-15 product bar is "understand the system", not "restate
 * route_family/stage counts". A purely mechanical template (e.g.
 *   title = journeyLabel(route_family); body = `${stages} stages across
 *   ${subjects}`)
 * regresses to exactly the JSONL-field-dump the spec calls a failure. So this
 * module carries HONEST, profile-authored narrative content: Bigtop is taught
 * as a BOM->recipe->provisioner->smoke->runtime distribution whose confidence
 * stops at static source. The narratives are GENERATED by these functions from
 * the real artifact set (stage roles, evidence states, runtime assessments,
 * attached finding/probe ids, coverage subjects) — they are not hand-written
 * into final HTML — and they degrade honestly when the underlying artifacts are
 * sparse or absent.
 *
 * PURE: no DOM, no I/O, no fs. Domain layer — zero dependencies. Anchor-status
 * classification here works off fields already present on the parsed nav-atlas;
 * the richer snippet/anchor resolution happens export-time in the adapter and is
 * threaded back in via enriched in-memory rows (source_excerpt / anchor_status).
 */
