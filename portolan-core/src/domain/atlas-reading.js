/**
 * Pick the top N probes for the first screen. Priority: blocked before
 * not_assessed (blocked is the stronger "we tried and could not"), then by
 * probe_risk (medium/high first — these are the ones an admiral cares about),
 * preserving stable order otherwise.
 */
function topProbes(probes, n) {
  const riskRank = { high: 0, medium: 1, low: 2 };
  const stateRank = { blocked: 0, failed: 1, cannot_verify: 2, not_assessed: 3, unknown: 4 };
  // NOTE: rank 0 is a real priority ('blocked', 'high'); use nullish checks,
  // never `|| 9`, which would coerce 0 to 9 and invert the priority.
  const r = (map, v) => (v in map ? map[v] : 9);
  return [...(probes || [])]
    .sort((a, b) => {
      const s = r(stateRank, a.state) - r(stateRank, b.state);
      if (s) return s;
      return r(riskRank, a.probe_risk) - r(riskRank, b.probe_risk);
    })
    .slice(0, n || 3);
}

/**
 * Pick the top N findings for the first screen. Priority: severity (critical ->
 * major -> minor -> info), preserving stable order.
 */
function topFindings(findings, n) {
  const sevRank = { critical: 0, major: 1, minor: 2, info: 3 };
  const r = (v) => (v in sevRank ? sevRank[v] : 9);
  return [...(findings || [])]
    .sort((a, b) => r(a.severity) - r(b.severity))
    .slice(0, n || 3);
}

/**
 * Build the agent-handoff query list (doc 15 §10). Each entry has a stable id,
 * a label, and the concrete command a follow-up agent would run via the query
 * surface. These are generated from the real artifact set (real route ids,
 * receipt target) so they are runnable, not placeholder text.
 */
function handoffQueries(navAtlas) {
  const ni = (navAtlas && navAtlas.navigationIndex) || [];
  const rv = (navAtlas && navAtlas.receiptValidation) || {};
  const routeIds = [...new Set(ni.map(s => s.route_id))];
  const packageRoute = routeIds.find(r => r.includes('package')) || routeIds[0] || '';
  const target = rv.target_id || '';
  const q = (id, label, command) => ({ id, label, command });
  return [
    q('handoff:list-routes', 'List journeys / routes',
      'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> list-routes'),
    q('handoff:open-package-route', 'Open the package-distribution route',
      packageRoute
        ? 'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> route --id ' + packageRoute
        : 'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> list-routes'),
    q('handoff:list-probes', 'List unknown probes',
      'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> list-probes'),
    q('handoff:list-findings', 'List findings for the package route',
      packageRoute
        ? 'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> route --id ' + packageRoute
        : 'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> list-findings'),
    q('handoff:receipt', 'Show receipt validation',
      'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> receipt'),
    q('handoff:coverage-gaps', 'Show coverage gaps',
      'node portolan-core/scripts/query-atlas-navigation.mjs --bundle <bundle> coverage'),
  ].map(x => ({ ...x, target }));
}

