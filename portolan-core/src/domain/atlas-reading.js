/**
 * Bucket the coverage matrix into reading regions. Derives central/peripheral
 * from whether a subject carries any route coverage (central) or not
 * (peripheral). Returns counts + representative subjects for each region so the
 * coverage surface shows scale, not a flat subject table.
 */
function coverageRegions(cm) {
  const covered = [];     // route_status complete
  const partial = [];     // route_status partial
  const routeless = [];   // no meaningful route (route_status missing/not_assessed/blocked)
  const missing = [];     // promotion_state missing / not_assessed (CROSS-CUTTING lens)
  // The primary route buckets (covered/partial/routeless) are mutually exclusive
  // and exhaustive: every subject lands in EXACTLY one, so their counts sum to
  // total. `missing` is a SEPARATE promotion lens — a subject can be `missing`
  // (promotion) AND e.g. `routeless` (route_status); it is reported as its own
  // axis, never added into the primary partition, so the counts never imply
  // missing+covered+... = total.
  for (const c of cm) {
    const rs = c.route_status || 'not_assessed';
    if (c.promotion_state === 'missing' || c.promotion_state === 'not_assessed') missing.push(c);
    if (rs === 'complete') covered.push(c);
    else if (rs === 'partial') partial.push(c);
    else routeless.push(c);
  }
  // central = has any route ref; peripheral = no route ref at all.
  const central = cm.filter(c => c.route_refs && c.route_refs.length);
  const peripheral = cm.filter(c => !(c.route_refs && c.route_refs.length));
  return {
    covered, partial, routeless, missing, central, peripheral,
    counts: {
      total: cm.length,
      covered: covered.length,
      partial: partial.length,
      routeless: routeless.length,
      missing: missing.length,
      central: central.length,
      peripheral: peripheral.length,
    },
  };
}

