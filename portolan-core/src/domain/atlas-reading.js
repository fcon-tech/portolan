/**
 * Build a linear route diagram (ordered stages -> nodes, sequential flow -> edges)
 * from a route's stages. No graph library; the shell renders nodes/edges as a
 * wrapped flex flow with arrows. Each node carries role/evidence/runtime/finding
 * /probe signals so the diagram is legible without a table.
 *
 * @param {Array<object>} stages ordered navigation-index stage rows for one route
 * @returns {{nodes:Array, edges:Array}}
 */
function buildRouteDiagram(stages) {
  const sorted = [...stages].sort((a, b) => a.stage_index - b.stage_index);
  const nodes = sorted.map(s => ({
    stageIndex: s.stage_index,
    label: stageLabel(s),
    role: PATH_ROLE_LABELS[s.path_role] || s.path_role || 'Stage',
    roleKey: s.path_role || 'unknown',
    evidenceState: EVIDENCE_STATE_LABELS[s.source_evidence_state] || s.source_evidence_state || 'Not assessed',
    runtimeAssessment: s.runtime_assessment || 'not_assessed',
    findingCount: (s.finding_refs || []).length,
    probeCount: (s.unknown_probe_refs || []).length,
    anchorStatus: anchorStatus(s),
  }));
  const edges = [];
  for (let i = 0; i < nodes.length - 1; i++) edges.push({ from: nodes[i].stageIndex, to: nodes[i + 1].stageIndex });
  return { nodes, edges };
}

/**
 * Human-readable stage label: prefer the stage's own name prettified, fall back
 * to its role label. Never just the raw snake_case id.
 */
function stageLabel(s) {
  if (s && s.stage && /^[a-z0-9_-]+$/.test(s.stage)) {
    return s.stage
      .split(/[-_]/)
      .filter(Boolean)
      .map(w => w.charAt(0).toUpperCase() + w.slice(1))
      .join(' ');
  }
  return (s && s.stage) || PATH_ROLE_LABELS[(s && s.path_role)] || 'Stage';
}

/**
 * Resolve the reading thesis for a route (what this route explains). Uses
 * profile-authored copy when available; otherwise synthesizes an honest,
 * generic-but-meaningful line from the route family + stage roles (NOT a bare
 * field restatement).
 */
function routeThesis(route, stages) {
  if (route && ROUTE_THESES[route.route_id]) return ROUTE_THESES[route.route_id];
  const roles = [...new Set((stages || []).map(s => PATH_ROLE_LABELS[s.path_role] || s.path_role).filter(Boolean))];
  if (roles.length) {
    return 'This route traces a ' + roles.join(' -> ') + ' path through the system. Each stage is ' +
      'source-visible where anchored; runtime/build/test confidence is recorded per stage.';
  }
  return 'This route traces an ordered system path. Open the diagram to see its stages and where confidence stops.';
}

/**
 * Classify a stage's anchor quality for honest rendering.
 *
 * Priority:
 *  1. If the row carries an export-time-enriched `anchor_status`, trust it
 *     (adapter resolved the file precisely).
 *  2. `line_start > 0` -> 'precise' (a real line anchor exists).
 *  3. `line_start === 0` + a quality note mentioning ambiguity -> 'ambiguous'.
 *  4. `line_start === 0` + a quality note mentioning a missing anchor -> 'missing'.
 *  5. Otherwise 'unresolved' (we simply cannot tell).
 *
 * This NEVER fabricates a precise anchor: a 0/0 line range is never rendered as
 * a precise line number, even if source_excerpt somehow appears.
 */
function anchorStatus(stage) {
  if (stage && stage.anchor_status) return stage.anchor_status;
  const ls = Number(stage && stage.line_start);
  const le = Number(stage && stage.line_end);
  // A precise anchor requires BOTH bounds > 0 (1-indexed range). A row with
  // line_start 0 but line_end 42 is nonsensical and must not be 'precise'.
  if (ls > 0 && le > 0) return 'precise';
  const note = String((stage && stage.route_quality_note) || '').toLowerCase();
  if (note.includes('ambig')) return 'ambiguous';
  if (note.includes('not found') || note.includes('missing') || note.includes('no anchor')) return 'missing';
  return 'unresolved';
}

/**
 * Plain-language explanation for an anchor status. Used in the dossier so a
 * missing/ambiguous anchor is explained in words, not hidden in a badge, and a
 * precise anchor is shown with its snippet.
 *
 * @returns {string|null} null when a snippet should be shown instead (precise)
 */
function anchorExplanation(stage) {
  const status = anchorStatus(stage);
  switch (status) {
    case 'precise':
      return null; // a snippet is shown instead
    case 'ambiguous':
      return 'Ambiguous anchor: the source location could not be pinned to a single line, so no precise line range is shown. The stage is still source-visible at the path above.';
    case 'missing':
      return 'Anchor not found: the referenced source location could not be resolved. The stage is kept visible so the gap is honest, not hidden.';
    case 'missing-file':
      return 'Missing source file: the referenced path does not exist in the target. The stage is kept visible so the gap is honest, not hidden.';
    default:
      return 'Unresolved anchor: no precise source location is available. The stage is kept visible; treat its source path as a pointer, not a verified location.';
  }
}

